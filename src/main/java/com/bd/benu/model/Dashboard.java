package com.bd.benu.model;

public class Dashboard {
	
	private Integer totalCount;
	
	private Integer enrolleCount;
	
	private Integer lostCount;
	
	private Integer openCount;
	
	private Integer counsellorI;

	public Integer getTotalCount() {
		return totalCount;
	}

	public void setTotalCount(Integer totalCount) {
		this.totalCount = totalCount;
	}

	public Integer getEnrolleCount() {
		return enrolleCount;
	}

	public void setEnrolleCount(Integer enrolleCount) {
		this.enrolleCount = enrolleCount;
	}

	public Integer getLostCount() {
		return lostCount;
	}

	public void setLostCount(Integer lostCount) {
		this.lostCount = lostCount;
	}

	public Integer getOpenCount() {
		return openCount;
	}

	public void setOpenCount(Integer openCount) {
		this.openCount = openCount;
	}

	public Integer getCounsellorI() {
		return counsellorI;
	}

	public void setCounsellorI(Integer counsellorI) {
		this.counsellorI = counsellorI;
	}

	
}
